package cz.muni.PV260.tron;

public class ControlKeys {
    final int keyUp;
    final int keyDown;
    final int keyLeft;
    final int keyRight;

    public ControlKeys(int keyUp, int keyDown, int keyLeft, int keyRight) {
        this.keyUp = keyUp;
        this.keyDown = keyDown;
        this.keyLeft = keyLeft;
        this.keyRight = keyRight;
    }
}
